// Populate the table with player data
function populateTable(data) {
    const tableBody = document.getElementById('tableBody');
    tableBody.innerHTML = '';

    data.forEach(player => {
        const row = `<tr>
            <td>${player.Player}</td>
            <td>${player.Player_type}</td>
            <td>${player.Predicted_Overall_score.toFixed(2)}</td>
        </tr>`;
        tableBody.insertAdjacentHTML('beforeend', row);
    });
}

// Filter function based on input
function filterPlayers() {
    const input = document.getElementById('playerInput').value.toLowerCase();
    const filteredData = playerData.filter(player =>
        player.Player.toLowerCase().includes(input)
    );
    populateTable(filteredData);

    const playerScoreDiv = document.getElementById('playerScore');
    if (filteredData.length === 1) {
        playerScoreDiv.innerHTML = `Player: ${filteredData[0].Player}, Score: ${filteredData[0].Predicted_Overall_score.toFixed(2)}`;
    } else {
        playerScoreDiv.innerHTML = '';
    }
}

// Clear the input field and show all data
function clearInput() {
    document.getElementById('playerInput').value = '';
    document.getElementById('playerScore').innerHTML = '';
    populateTable(playerData);
}

// Sorting functionality for the table
function sortTable(columnIndex) {
    const table = document.getElementById("playerTable");
    const rows = Array.from(table.rows).slice(1);
    const isAscending = table.getAttribute("data-sort-dir") === "asc";

    rows.sort((row1, row2) => {
        const cell1 = row1.cells[columnIndex].innerText.toLowerCase();
        const cell2 = row2.cells[columnIndex].innerText.toLowerCase();

        if (!isNaN(cell1) && !isNaN(cell2)) {
            return isAscending ? cell1 - cell2 : cell2 - cell1;
        }
        return isAscending ? cell1.localeCompare(cell2) : cell2.localeCompare(cell1);
    });

    table.setAttribute("data-sort-dir", isAscending ? "desc" : "asc");
    rows.forEach(row => table.tBodies[0].appendChild(row));
}

